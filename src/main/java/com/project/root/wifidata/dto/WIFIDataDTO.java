package com.project.root.wifidata.dto;

public class WIFIDataDTO {
	private int wifiNo;
	private String basNo;
	private String wifiName;
	private String wifiPw;
	
	
	public int getWifiNo() {
		return wifiNo;
	}
	public void setWifiNo(int wifiNo) {
		this.wifiNo = wifiNo;
	}
	public String getBasNo() {
		return basNo;
	}
	public void setBasNo(String basNo) {
		this.basNo = basNo;
	}
	public String getWifiName() {
		return wifiName;
	}
	public void setWifiName(String wifiName) {
		this.wifiName = wifiName;
	}
	public String getWifiPw() {
		return wifiPw;
	}
	public void setWifiPw(String wifiPw) {
		this.wifiPw = wifiPw;
	}
	
	
}
