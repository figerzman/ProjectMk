package com.project.root.wifidata.dto;

public class BasicWIFIDataDTO {
	
	private String basNo;
	private String basName;
	private int catNo;
	private String basAddr;
	private String basLat;
	private String basLng;
	private int wifiNo;
	private String wifiName;
	private String wifiPw;
	
	public String getBasNo() {
		return basNo;
	}
	public void setBasNo(String basNo) {
		this.basNo = basNo;
	}
	public String getBasName() {
		return basName;
	}
	public void setBasName(String basName) {
		this.basName = basName;
	}
	public int getCatNo() {
		return catNo;
	}
	public void setCatNo(int catNo) {
		this.catNo = catNo;
	}
	public String getBasAddr() {
		return basAddr;
	}
	public void setBasAddr(String basAddr) {
		this.basAddr = basAddr;
	}
	public String getBasLat() {
		return basLat;
	}
	public void setBasLat(String basLat) {
		this.basLat = basLat;
	}
	public String getBasLng() {
		return basLng;
	}
	public void setBasLng(String basLng) {
		this.basLng = basLng;
	}
	public int getWifiNo() {
		return wifiNo;
	}
	public void setWifiNo(int wifiNo) {
		this.wifiNo = wifiNo;
	}
	public String getWifiName() {
		return wifiName;
	}
	public void setWifiName(String wifiName) {
		this.wifiName = wifiName;
	}
	public String getWifiPw() {
		return wifiPw;
	}
	public void setWifiPw(String wifiPw) {
		this.wifiPw = wifiPw;
	} 
	
	

}
